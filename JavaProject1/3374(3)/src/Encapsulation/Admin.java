package Encapsulation;

public class Admin {
     private String name; private int id; private int age;


     public void setName(String n){
            name=n;
        }
    public void setId(int i){
        id=i;
    }
    public void setAge(int a){
        age=a;
    }

        public String getName() {
            return name;
        }
    public int getId() {
        return id;
    }
    public int getAge() {
        return age;
    }


    }

